
public class HelloJava {

	public static void main(String[] args) {
		// 코드를 작성하는 블락 
		
		// 변수 선언 
		String a = "안녕 "; 
		String b = "하세요"; 
		
		// 변수의 내용을 출력 
		System.out.println(a);  // 출력후 엔터가 적용됨 
		System.out.println(b);  // 출력후 엔터가 적용됨 
		
		System.out.println("출력할 내용 ~~~"); 
		// 단축키 : syso  ctrl+space , enter 
		System.out.println("=====================");
		System.out.print(a);    // 출력후 엔터가 없음
		System.out.print(b);    // 출력후 엔터가 없음 
		
	}

}
